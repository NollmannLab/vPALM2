%% clear m_matrices
function clear_m_matrices()

        if exist('h.m')==1
            clear h.m;
        end
        
        if exist('h.m_drift')==1
            clear h.m_drift;
        end
        
        if exist('h.m2')==1
            clear h.m2;
        end

        if exist('m')==1
            clear m;
        end
